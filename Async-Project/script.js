function getCountry(country) {
  const request = new XMLHttpRequest();
  request.open('GET', `https://restcountries.com/v3.1/name/${country}`);
  request.send();

  request.addEventListener('load', function () {
    const [data] = JSON.parse(this.responseText);
    console.log(data);

    const countryCurrencyCode = Object.keys(data.currencies)[0];
    const { name, symbol } = data.currencies[countryCurrencyCode];

    const htmlMarkUp = `
  <article class="country">
          <img class="country__img" src="${data.flags.png}" />
          <div class="country__data">
            <h3 class="country__name">${data.name.common}</h3>
            <h4 class="country__region">${data.region}</h4>
            <p class="country__row"><span>üë´</span>${(
              +data.population / 1000000
            ).toFixed(1)} million people</p>
            <p class="country__row"><span>üó£Ô∏è</span>${
              Object.values(data.languages)[0]
            }</p>
            <p class="country__row"><span>üí∞</span>${name} - ${symbol}</p>
          </div>
    </article>
  `;

    countriesContainer.insertAdjacentHTML('beforeend', htmlMarkUp);
    countriesContainer.style.opacity = 1;
  });
}

getCountry('ghana');
getCountry('nigeria');
getCountry('spain');
getCountry('america');
